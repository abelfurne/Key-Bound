                If it is your first time being here, you will need to create an account.
                Click "Login" on the navigation bar to be redirected to the login page.
                This is where you have the options of logging in or creating account.
                After creating your account, it is time to add some keybinds!<br><br>

                Head over to the Keybinds page and start adding some keybinds! You will be
                met with a page that gives the options to create a new table and, within
                that table, add the names, icons, and keybinds of your chosen spells or
                abilities. Icons are optional. If there is no icon, the spell's name will
                replace the icon.<br><br>

                Once you have the table set up, head to the Practice page and select the
                table to start practicing! Once the table is loaded, the only thing you
                need to do to start practicing is hit the space bar!<br><br>

                A timer will start after you hit the space bar and will count until you
                correctly or incorrectly input the keybind stored in the table for that
                spell or ability. Once the timer stops, you will be given a "Correct!" or
                "Incorrect!" message, the correct keybind, and your record for correctly
                inputting that keybind. To continue to the next keybind, press the space
                bar again.<br><br>

                If you correctly input the keybind, that keybind will be removed from the
                list of keybinds you will be tested on during that session. If you
                incorrectly input the keybind, that keybind will be moved to the back of
                the list for you to redo.
